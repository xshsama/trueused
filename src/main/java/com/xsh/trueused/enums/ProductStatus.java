package com.xsh.trueused.enums;

/**
 * 商品状态枚举 (只关注销售生命周期)
 */
public enum ProductStatus {
    // 1. 不可售阶段 (包含了 待发货、运输中、验货中、验货失败)
    // 对应前端逻辑：不在商城列表展示，或者展示在"即将上架"
    PENDING,

    // 2. 可售阶段
    ON_SALE, // 已上架 (验货通过，且未被锁定)

    // 3. 交易锁定阶段
    LOCKED, // 交易中 (有买家下单但未付款，或正在支付中)

    // 4. 终态
    SOLD, // 已售出 (支付成功)
    OFF_SHELF // 已下架 (卖家取回、违规下架、管理员删除)
}
